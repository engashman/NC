// Function to calculate nutrition
function calculateNutrition() {
    const gender = document.getElementById('gender').value;
    const ageGroup = document.getElementById('ageGroup').value;
    const nutrient = document.getElementById('nutrient').value;
    const subSelection = document.getElementById('subSelection').value;

    const nutritionResult = getNutritionResult(gender, ageGroup, nutrient, subSelection);

    const resultElement = document.getElementById('result');
    resultElement.innerHTML = formatNutritionResults(nutritionResult);
}

// Function to format nutrition results for display
function formatNutritionResults(nutritionResult) {
    let formattedResult = '';

    // Check if the nutrient has sub-properties
    if (typeof nutritionResult === 'object') {
        // Nutrient has sub-properties, display them
        Object.keys(nutritionResult).forEach(property => {
            formattedResult += `<span style="color: blue;">${formatNutrientName(property)}:</span> <span style="color: red;">${nutritionResult[property]}</span><br>`;
        });
    } else {
        // Nutrient does not have sub-properties, display the result directly
        formattedResult = `<span style="color: blue;">${formatNutrientName(nutrient)}:</span> <span style="color: red;">${nutritionResult}</span><br>`;
    }

    return formattedResult.trimEnd(); // Trim to remove trailing newline
}


// Function to format sub-nutrient values
function formatSubNutrientValue(value) {
    if (typeof value === 'object') {
        // If the value is still an object, handle it accordingly
        return formatNutritionResults(value);
    } else {
        // Otherwise, display the value
        return value;
    }
}

// Function to format nutrient names for display
function formatNutrientName(nutrient) {
    const nutrientNames = {
        rda: 'Recommended Daily Allowance (RDA)',
        ai: 'Adequate Intake (AI)',
        ul: 'Tolerable Upper Intake Level (UL)',
        eer: 'Estimated Energy Requirement (EER)',
    };

    return nutrientNames[nutrient] || nutrient;
}

// Function to get nutrition result based on user input
function getNutritionResult(gender, ageGroup, nutrient, subSelection) {
    // Check if the nutrient has sub-properties
    if (subSelection) {
        // Nutrient has sub-properties, return the specific sub-property value
        return nutritionData[gender][ageGroup][nutrient][subSelection];
    } else {
        // Nutrient does not have sub-properties, return the overall value
        return nutritionData[gender][ageGroup][nutrient];
    }
}

// Function to handle nutrient selection change
function handleNutrientSelection() {
    const nutrient = document.getElementById('nutrient').value;
    const subSelectionSection = document.getElementById('subSelectionSection');
    const subSelection = document.getElementById('subSelection');

    // Clear existing options
    subSelection.innerHTML = '';

    // Populate sub-selection based on the selected nutrient
    if (nutrient === 'vitamins' || nutrient === 'minerals') {
        const ageGroup = document.getElementById('ageGroup').value;
        const gender = document.getElementById('gender').value;

        // Check if the nutrient and age group are valid
        if (nutritionData[gender] && nutritionData[gender][ageGroup] && nutritionData[gender][ageGroup][nutrient]) {
            const subOptions = Object.keys(nutritionData[gender][ageGroup][nutrient]);
            populateDropdown(subSelection, subOptions);
            subSelectionSection.style.display = 'block';
        } else {
            subSelectionSection.style.display = 'none';
        }
    } else {
        subSelectionSection.style.display = 'none';
    }

    // Show or hide pregnancy and lactation sections based on gender
    updatePregnancyLactationSections();
}

// Function to populate dropdown with options
function populateDropdown(element, options) {
    options.forEach(option => {
        const optionElement = document.createElement('option');
        optionElement.value = option;
        optionElement.textContent = option;
        element.appendChild(optionElement);
    });
}

// Function to populate age options for selected gender
function populateAgeOptions() {
    const ageGroupSelect = document.getElementById('ageGroup');
    ageGroupSelect.innerHTML = '';

    const genderSelect = document.getElementById('gender');
    const selectedGender = genderSelect.value;

    let ageGroups;

    if (selectedGender === 'male' || selectedGender === 'female') {
        ageGroups = ['0-0.5', '0.5-1', '1-3', '4-8', '9-13', '14-18', '19-30', '31-50', '51-70', '71+'];
    } else if (selectedGender === 'pregnant1' || selectedGender === 'pregnant2' || selectedGender === 'pregnant3' || selectedGender === 'lactating1' || selectedGender === 'lactating2') {
        ageGroups = ['14-18', '19-30', '31-50'];
    }

    ageGroups.forEach(ageGroup => {
        const optionElement = document.createElement('option');
        optionElement.value = ageGroup;
        optionElement.textContent = ageGroup;
        ageGroupSelect.appendChild(optionElement);
    });
}
// Initialization function
function init() {
    populateAgeOptions();
    handleNutrientSelection(); // Call this function to set up sub-selection on page load
}

// Attach event listeners
document.getElementById('nutrient').addEventListener('change', handleNutrientSelection);
document.getElementById('ageGroup').addEventListener('change', handleNutrientSelection);
document.getElementById('gender').addEventListener('change', () => {
    updatePregnancyLactationSections();
    handleNutrientSelection();
});

// Run the initialization function when the page loads
document.addEventListener('DOMContentLoaded', init);
